package javaExam;

public class Ex02 {

	public static void main(String[] args) {
		//1부터 10까지 더하기
		int sum = 0;
		for(int i=1; i<=10; i++) {
			sum += i;
		}
		System.out.println(sum);
		
		
		int j=1;
		int jsum=0;
		while(j<=10) {
			jsum += j;
			j++;
		}
		System.out.println(jsum);
	
	
	
	}

}
